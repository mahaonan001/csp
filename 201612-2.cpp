#include<iostream>
using namespace std;
int main() {
	int T;
	cin >> T;
	int s;
	if (T <= 3500) {
		s = T;
	}
	else{
		T = T - 3500;
		if (T <= 1500 * 0.97) {
			s = T / 0.97;
		}
		else if (T <= 1500 * 0.97 + 3000 * 0.9) {
			s = (T - 1500 * 0.97) / 0.9 + 1500;
		}
		else if (T <= 1500 * 0.97 + 3000 * 0.9 + 4500 * 0.8) {
			s = (T - 1500 * 0.97 - 3000 * 0.9) / 0.8 + 1500 + 3000;
		}
		else if (T <= 1500 * 0.97 + 3000 * 0.9 + 4500 * 0.8 + 26000 * 0.75) {
			s = (T - 1500 * 0.97 - 3000 * 0.9 - 4500 * 0.8) / 0.75 + 1500 + 3000 + 4500;
		}
		else if (T <= 1500 * 0.97 + 3000 * 0.9 + 4500 * 0.8 + 26000 * 0.75 + 20000 * 0.7) {
			s = (T - 1500 * 0.97 - 3000 * 0.9 - 4500 * 0.8 - 26000 * 0.75) / 0.7 + 1500 + 3000 + 4500 + 26000;
		}
		else if (T <= 1500 * 0.97 + 3000 * 0.9 + 4500 * 0.8 + 26000 * 0.75 + 20000 * 0.7 + 25000 * 0.65) {
			s = (T - 1500 * 0.97 - 3000 * 0.9 - 4500 * 0.8 - 26000 * 0.75 - 20000 * 0.7) / 0.65 + 1500 + 3000 + 4500 + 26000 + 20000;
		}
		else if (T > 1500 * 0.97 + 3000 * 0.9 + 4500 * 0.8 + 26000 * 0.75 + 20000 * 0.7 + 25000 * 0.65) {
			s = (T - 1500 * 0.97 - 3000 * 0.9 - 4500 * 0.8 - 26000 * 0.75 - 20000 * 0.7 - 25000 * 0.65) / 0.55 + 1500 + 3000 + 4500 + 26000 + 20000 + 25000;
		}
		s += 3500;
	}
	cout << s << endl;
}
